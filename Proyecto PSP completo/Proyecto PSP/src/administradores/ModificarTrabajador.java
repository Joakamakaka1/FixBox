    public ModificarTrabajador() {
        initComponents();
        setResizable(false);
        setLocationRelativeTo(null);

        Connection cn = ConectarBD.conectar();
        try {
            PreparedStatement ps = cn.prepareStatement("SELECT * FROM TRABAJADORES WHERE ID_TRABAJADORES = ?");
            ps.setString(1, ConsultarTrabajador.idSeleccionado);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                campoDNI.setText(rs.getString(2));
                campoUsuario.setText(rs.getString(3));
                campoContraseña.setText(rs.getString(4));
                campoNombre.setText(rs.getString(5));
                campoApellido.setText(rs.getString(6));
                campoEdad.setText(rs.getString(7));
                campoFechaIngreso.setText(rs.getString(8));
                jornada = rs.getString(9);
                campoSalario.setText(rs.getString(10));
                estado = rs.getString(11);
                tipo = rs.getString(12);

            }
            
            if (jornada.equals("Completa")) {
                comboBoxJornada.setSelectedIndex(0);
            } else {
                comboBoxJornada.setSelectedIndex(1);
            }

            if (tipo.equals("Administrador")) {
                comboBoxTipo.setSelectedIndex(0);
            } else if (tipo.equals("Recepcionista")) {
                comboBoxTipo.setSelectedIndex(1);
            } else {
                comboBoxTipo.setSelectedIndex(2);
            }

            if (estado.equals("Activo")) {
                rbActivado.setSelected(true);
            } else {
                rbDesactivado.setSelected(true);
            }

        } catch (SQLException e) {
            System.err.println("Error en la muestra de los datos " + e.getMessage());
        }
    setIconImage(getIconImage());
    }
