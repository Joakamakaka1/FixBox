    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     // TODO add your handling code here:

        if (rbActivado.isSelected()) {
            estado = "Activo";
        }
        if (rbDesactivado.isSelected()) {
            estado = "Inactivo";
        }

        // Convertir el arreglo de caracteres a String correctamente
        String contraseña = new String(campoContraseña.getPassword());

        try {
            Connection cn = ConectarBD.conectar();
            String consulta = "UPDATE TRABAJADORES SET Usuario = ?, Pass = ?, Nombre = ?, Apellidos = ?, Edad = ?, FechaIngreso = ?, TipoJornada = ?, Salario = ?, EstadoActual = ?, Tipo = ? WHERE DNI = ?";
            PreparedStatement ps = cn.prepareStatement(consulta);

            ps.setString(1, campoUsuario.getText());
            ps.setString(2, contraseña);
            ps.setString(3, campoNombre.getText());
            ps.setString(4, campoApellido.getText());
            ps.setString(5, campoEdad.getText());
            ps.setString(6, campoFechaIngreso.getText());
            ps.setString(7, comboBoxJornada.getSelectedItem().toString());
            ps.setString(8, campoSalario.getText());
            ps.setString(9, estado);
            ps.setString(10, comboBoxTipo.getSelectedItem().toString());
            ps.setString(11, campoDNI.getText());

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Trabajador actualizado con éxito");
            ConsultarTrabajador ct = new ConsultarTrabajador();
            ct.setVisible(true);
            this.dispose();
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en la actualización del trabajador: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

