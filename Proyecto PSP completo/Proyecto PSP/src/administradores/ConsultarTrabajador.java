    public ConsultarTrabajador() {
        initComponents();
        setTitle("Consultar trabajadores");
        setLocationRelativeTo(null);
        setResizable(false);

        try {
            Connection cn = ConectarBD.conectar();
            PreparedStatement ps = cn.prepareStatement("SELECT * FROM TRABAJADORES");
            ResultSet rs = ps.executeQuery();

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setRowCount(0);
            modelo.addColumn("ID");
            modelo.addColumn("DNI");
            modelo.addColumn("Usuario");
            modelo.addColumn("Contrase単a");
            modelo.addColumn("Nombre");
            modelo.addColumn("Apellidos");
            modelo.addColumn("Edad");
            modelo.addColumn("Fecha ingreso");
            modelo.addColumn("Jornada");
            modelo.addColumn("Salario");
            modelo.addColumn("Estado");
            modelo.addColumn("Tipo");

            while (rs.next()) {
                id = rs.getString(1);
                dni = rs.getString(2);
                usuario = rs.getString(3);
                contrase単a = rs.getString(4);
                nombre = rs.getString(5);
                apellidos = rs.getString(6);
                edad = rs.getInt(7);
                fechaIngreso = rs.getString(8);
                jornada = rs.getString(9);
                salario = rs.getFloat(10);
                estado = rs.getString(11);
                tipo = rs.getString(12);

                modelo.addRow(new Object[]{id, dni, usuario, contrase単a, nombre, apellidos, edad, fechaIngreso, jornada, salario, estado, tipo});

            }

            tdConsultarTrabajador.setModel(modelo);
            tdConsultarTrabajador.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            tdConsultarTrabajador.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (!e.getValueIsAdjusting()) {
                        int filaSeleccionada = tdConsultarTrabajador.getSelectedRow();

                        idSeleccionado = tdConsultarTrabajador.getValueAt(filaSeleccionada, 0).toString();

                        ModificarTrabajador modT = new ModificarTrabajador();
                        modT.setVisible(true);
                        dispose();

                    }

                }

            });

            sorter = new TableRowSorter<>(modelo);
            tdConsultarTrabajador.setRowSorter(sorter);
            cn.close();
        } catch (SQLException e) {
            System.err.println("Error en la conexion a la BBDD" + e.getMessage());
        }

        CampoDNI.getDocument().addDocumentListener(new DocumentListener() {
            public void changedUpdate(DocumentEvent e) {
                filter();
            }

            public void removeUpdate(DocumentEvent e) {
                filter();
            }

            public void insertUpdate(DocumentEvent e) {
                filter();
            }

            public void filter() {
                String text = CampoDNI.getText();
                if (text.trim().length() == 0) {
                    sorter.setRowFilter(null);
                } else {
                    sorter.setRowFilter(RowFilter.regexFilter("^" + text, 1));
                }
            }
        });
    setIconImage(getIconImage());
    }
