    public void crearIncidencia() {

        String descripcion = area.getText();
        String nombre = fieldnombre.getText();
        LocalDate fechaalta = LocalDate.now();

        try {
            Connection cn = ConectarBD.conectar();
            String sql = "INSERT INTO Incidencias (Descripcion,Estado,Nombre,FechaApertura,ID_CLIENTES) VALUES(?,?,?,?,?)";
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, descripcion);
            ps.setString(2, "Incidencia Recibida");
            ps.setString(3, nombre);
            ps.setDate(4, java.sql.Date.valueOf(fechaalta));
            ps.setInt(5, Integer.parseInt(this.labelid.getText()));
            ps.executeUpdate();
             JOptionPane.showMessageDialog(null, "Incidencia creada");
            this.dispose();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }

    

